public class KodeBUah
{
    public string GetKodeBuah (String namaBuah)
    {
        if (namaBuah == "apel")
        {
            return "A00";
        }
        else if (namaBuah == "Aprikot")
        {
            return "B00";
        }
        else if (namaBuah == "Alpukat")
        {
            return "C00";
        }
        else if (namaBuah == "Pisang")
        {
            return D00;
        } else if (namaBuah == "Paprika")
        {
            return E00;
        }
    }
}
